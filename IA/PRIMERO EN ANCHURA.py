def dfs_paths(graph, start, goal):
    stack = [(start, [start])]
    while stack:
        (vertex, path) = stack.pop()
        #print ('path: ' , path)
        l = list(set(graph[vertex]) - set(path))
        for next in sorted(l, reverse=True):
                        
            if next == goal:
                yield path + [next]
            else:
                stack.append((next, path + [next]))


